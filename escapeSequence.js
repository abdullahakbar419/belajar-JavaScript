// pembuatan textarea
document.writeln("<textarea cols='100' rows= '10'>");
// untuk enter menggunakan (\n)
document.write("Abdullah \n Akbar\n\n");
// untuk tab menggunakan (\t)
document.write("Abdullah \t Akbar\n\n");
// untuk / dalam string maka menggunakan (//) didalam string
document.write("\\Abdullah Akbar\\\n\n");
// untuk " dalam string maka menggunakan (/") didalam string
document.write('"jdfbdjfb"\n\n');
// untuk ' dalam string maka menggunakan (/') didalam string
document.write("'Abdullah Akbar'\n\n");
//tag penutup textarea
document.writeln("</textarea>");

